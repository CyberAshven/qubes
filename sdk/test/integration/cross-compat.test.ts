// 6. Merkle root compatibility
// ---------------------------------------------------------------------------
//
// Python: compute_merkle_root(hashes) using string concatenation + SHA-256.
// TS:     computeMerkleRoot(hashes) must produce identical results.

describe('6. Merkle root compatibility ↔ Python compute_merkle_root()', () => {
  for (const tc of vectors.merkleRoot.cases) {
    it(`${tc.name}: root matches Python vector`, () => {
      const root = computeMerkleRoot(tc.hashes);
      expect(root).toBe(tc.root);
    });
  }

  it('empty list returns 64 zero characters', () => {
    expect(computeMerkleRoot([])).toBe('0'.repeat(64));
  });

  it('single hash returns itself unchanged', () => {
    const hash = 'a'.repeat(64);
    expect(computeMerkleRoot([hash])).toBe(hash);
  });

  it('can verify a chain of block hashes from the compat vectors', () => {
    // Simulate a 2-block chain: genesis + one memory block
    const genesisHash = vectors.blockHashing.blockHash;
    const standardHash = vectors.blockSigningStandard.blockHash;
    const root = computeMerkleRoot([genesisHash, standardHash]);

    // Verify the root is deterministic
    expect(root).toBe(computeMerkleRoot([genesisHash, standardHash]));
    expect(root).toMatch(/^[0-9a-f]{64}$/);
  });

  it('verifyPackageIntegrity uses Merkle root to confirm chain tamper-detection', () => {
    // Construct a minimal package where we know the Merkle root
    const genesisHash = 'a'.repeat(64);
    const block1Hash = 'b'.repeat(64);
    const correctRoot = computeMerkleRoot([genesisHash, block1Hash]);

    const validPackage: QubePackageData = {
      metadata: {
        qubeId: 'TEST0001',
        qubeName: 'Test',
        publicKey: vectors.keyDerivation.publicKeyHex,
        chainLength: 2,
        merkleRoot: correctRoot,
        packageVersion: '1.0',
        packagedAt: 1735689600,
        packagedBy: 'test',
        hasNft: false,
      },
      genesisBlock: { block_hash: genesisHash },
      memoryBlocks: [{ block_hash: block1Hash }],
      chainState: {},
    };

    expect(verifyPackageIntegrity(validPackage)).toBe(true);

    // Tamper with a block hash — integrity check must fail
    const tamperedPackage = {
      ...validPackage,
      memoryBlocks: [{ block_hash: 'c'.repeat(64) }],
    };
    expect(verifyPackageIntegrity(tamperedPackage)).toBe(false);
  });
});

// ---------------------------------------------------------------------------
// 7. ECIES format compatibility
// ---------------------------------------------------------------------------
//
// Python: ephemeral_pubkey(33) || nonce(12) || ciphertext+tag
// TS:     eciesEncrypt() must produce the same layout.

describe('7. ECIES format compatibility ↔ Python ecies_encrypt()', () => {
  const RECIPIENT_PRIVATE_KEY = fromHex(vectors.ecies.recipientPrivateKeyHex);
  const RECIPIENT_PUBLIC_KEY = fromHex(vectors.ecies.recipientPublicKeyHex);

  it('Python-generated ECIES ciphertext is decryptable by TS eciesDecrypt()', async () => {
    const ciphertextBytes = fromHex(vectors.ecies.ciphertextHex);
    const decrypted = await eciesDecrypt(ciphertextBytes, RECIPIENT_PRIVATE_KEY);

    expect(new TextDecoder().decode(decrypted)).toBe(vectors.ecies.plaintext);
  });

  it('ciphertext layout is ephemeral_pubkey(33) || nonce(12) || ciphertext+tag', async () => {
    const plaintext = new TextEncoder().encode('test payload for format check');
    const ciphertext = await eciesEncrypt(plaintext, RECIPIENT_PUBLIC_KEY);

    // Minimum length: 33 + 12 + 16 (tag, no payload stripped) + payload
    expect(ciphertext.length).toBeGreaterThanOrEqual(33 + 12 + 16);

    // ephemeral pubkey at offset 0 must be compressed: prefix byte 02 or 03
    const prefixByte = ciphertext[0];
    expect(prefixByte === 0x02 || prefixByte === 0x03).toBe(true);
  });

  it('ephemeral pubkey prefix is 0x02 or 0x03 (compressed secp256k1)', async () => {
    const ciphertextBytes = fromHex(vectors.ecies.ciphertextHex);

    // First byte of the Python-generated ciphertext
    expect(ciphertextBytes[0] === 0x02 || ciphertextBytes[0] === 0x03).toBe(true);

    // First byte of the Python ephemeral public key from the vector
    const ephemeralPubKey = fromHex(vectors.ecies.ephemeralPublicKeyHex);
    expect(ephemeralPubKey[0] === 0x02 || ephemeralPubKey[0] === 0x03).toBe(true);

    // First 33 bytes of ciphertext must equal the ephemeral public key
    const embeddedPubKey = ciphertextBytes.slice(0, 33);
    expect(toHex(embeddedPubKey)).toBe(vectors.ecies.ephemeralPublicKeyHex);
  });

  it('nonce occupies bytes 33–44 (12 bytes) in Python ciphertext', () => {
    const ciphertextBytes = fromHex(vectors.ecies.ciphertextHex);
    const nonce = ciphertextBytes.slice(33, 33 + 12);
    expect(toHex(nonce)).toBe(vectors.ecies.nonceHex);
  });

  it('TS eciesEncrypt → eciesDecrypt round-trip with recipient keys', async () => {
    const plaintext = new TextEncoder().encode('round-trip test payload');
    const ciphertext = await eciesEncrypt(plaintext, RECIPIENT_PUBLIC_KEY);
    const recovered = await eciesDecrypt(ciphertext, RECIPIENT_PRIVATE_KEY);

    expect(new TextDecoder().decode(recovered)).toBe('round-trip test payload');
  });

  it('wrong private key fails decryption', async () => {
    const plaintext = new TextEncoder().encode('private key mismatch test');
    const ciphertext = await eciesEncrypt(plaintext, RECIPIENT_PUBLIC_KEY);

    // Use a different private key (the signing key from the compat tests)
    const wrongKey = PRIVATE_KEY;
    await expect(eciesDecrypt(ciphertext, wrongKey)).rejects.toThrow();
  });

  it('ciphertext total length is 33 + 12 + plaintext_length + 16', async () => {
    const msg = 'length check test payload';
    const plaintext = new TextEncoder().encode(msg);
    const ciphertext = await eciesEncrypt(plaintext, RECIPIENT_PUBLIC_KEY);

    const expectedLen = 33 + 12 + plaintext.length + 16;
    expect(ciphertext.length).toBe(expectedLen);
  });
});

