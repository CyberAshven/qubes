// ---------------------------------------------------------------------------
// 1. Block hash compatibility
// ---------------------------------------------------------------------------
//
// Python: hash_block(block) = SHA-256(json.dumps(block, sort_keys=True))
// TS:     hashBlock(block)  = SHA-256(canonicalStringify(block))
//
// Both implementations must agree on every vector in vectors.json.

describe('1. Block hash compatibility ↔ Python hash_block()', () => {
  it('genesis block hash matches the Python-generated vector', () => {
    const hash = hashBlock(vectors.blockHashing.block);
    expect(hash).toBe(vectors.blockHashing.blockHash);
    // Sanity-check the known value from the generate-vectors.py output
    expect(hash).toBe('5d67be372c67d2925abe720cc3252d152fb02cd7e746d4c7b7884bf0e90135a0');
  });

  it('standard block hash (block_number=1) matches the Python-generated vector', () => {
    const hash = hashBlock(vectors.blockSigningStandard.block);
    expect(hash).toBe(vectors.blockSigningStandard.blockHash);
    expect(hash).toBe('23f449eac9e2376c588ab3cf7a77454fae5c7fdb6b6b2f7e501b01730e587443');
  });

  it('hash is deterministic: same dict always produces the same hash', () => {
    const block = {
      block_number: 5,
      timestamp: '2025-06-01T12:00:00Z',
      qube_id: 'AABBCCDD',
      data: { content: 'determinism test', nested: { x: 1, y: 2 } },
      previous_hash: 'f'.repeat(64),
    };

    const hash1 = hashBlock(block);
    const hash2 = hashBlock(block);
    expect(hash1).toBe(hash2);
    expect(hash1).toMatch(/^[0-9a-f]{64}$/);
  });

  it('block_hash and signature fields are excluded from the hash input', () => {
    const base = {
      block_number: 1,
      timestamp: '2025-01-01T00:01:00Z',
      qube_id: 'F909521D',
      data: { message: 'hello world' },
      previous_hash: '5d67be372c67d2925abe720cc3252d152fb02cd7e746d4c7b7884bf0e90135a0',
    };

    const withExtra = {
      ...base,
      block_hash: 'some_existing_hash_that_must_not_affect_the_result',
      signature: 'some_existing_signature_that_must_not_affect_the_result',
    };

    expect(hashBlock(base)).toBe(hashBlock(withExtra));
  });
});

// ---------------------------------------------------------------------------
// 2. Signature cross-verification
// ---------------------------------------------------------------------------
//
// DER-encoded ECDSA signatures must be interoperable. Python's cryptography
// library produces DER format; the TS SDK must verify them and vice-versa.

describe('2. Signature cross-verification ↔ Python sign_block()', () => {
  it('Python genesis signature is verifiable by TS verifyBlockSignature()', () => {
    const block = vectors.blockSigningGenesis.block;
    const pythonSig = vectors.blockSigningGenesis.signature;
    const pubKey = fromHex(vectors.blockSigningGenesis.publicKeyHex);

    expect(verifyBlockSignature(block, pythonSig, pubKey)).toBe(true);
  });

  it('Python standard block signature is verifiable by TS verifyBlockSignature()', () => {
    const block = vectors.blockSigningStandard.block;
    const pythonSig = vectors.blockSigningStandard.signature;
    const pubKey = fromHex(vectors.blockSigningStandard.publicKeyHex);

    expect(verifyBlockSignature(block, pythonSig, pubKey)).toBe(true);
  });

  it('TS genesis signature is DER-encoded (starts with 0x30)', () => {
    const block = vectors.blockSigningGenesis.block;
    const sig = signBlock(block, PRIVATE_KEY);

    // DER SEQUENCE tag = 0x30
    const firstByte = parseInt(sig.substring(0, 2), 16);
    expect(firstByte).toBe(0x30);
  });

  it('TS standard signature is DER-encoded (starts with 0x30)', () => {
    const block = vectors.blockSigningStandard.block;
    const sig = signBlock(block, PRIVATE_KEY);

    const firstByte = parseInt(sig.substring(0, 2), 16);
    expect(firstByte).toBe(0x30);
  });

  it('TS genesis signature round-trips (sign → verify with same pubkey)', () => {
    const block = vectors.blockSigningGenesis.block;
    const sig = signBlock(block, PRIVATE_KEY);

    expect(verifyBlockSignature(block, sig, PUBLIC_KEY)).toBe(true);
  });

  it('TS standard signature round-trips (sign → verify with same pubkey)', () => {
    const block = vectors.blockSigningStandard.block;
    const sig = signBlock(block, PRIVATE_KEY);

    expect(verifyBlockSignature(block, sig, PUBLIC_KEY)).toBe(true);
  });

  it('invalid hex signature returns false (not throws)', () => {
    const block = vectors.blockSigningGenesis.block;
    expect(verifyBlockSignature(block, 'deadbeef', PUBLIC_KEY)).toBe(false);
  });

  it('signature from a different key does not verify', () => {
    // Generate a fresh random-ish key (different from PRIVATE_KEY)
    const otherKey = fromHex('a1b2c3d4e5f60718293a4b5c6d7e8f90a1b2c3d4e5f60718293a4b5c6d7e8f90');
    const block = vectors.blockSigningGenesis.block;
    const sig = signBlock(block, otherKey);

    // Must not verify against the original public key
    expect(verifyBlockSignature(block, sig, PUBLIC_KEY)).toBe(false);
  });
});

// ---------------------------------------------------------------------------
// 3. Package format compatibility
// ---------------------------------------------------------------------------
//
// Python: create_package() → [4 QUBE magic][1 version][12 nonce][N ciphertext+tag]
// TS:     createBinaryPackage() must produce the identical layout.
//
// .qube ZIP format: manifest.json (plain) + data.enc (AES-256-GCM, no nonce prepended).

function makePackageData(): QubePackageData {
  return {
    metadata: {
      qubeId: 'F909521D',
      qubeName: 'Cross Compat Qube',
      publicKey: vectors.keyDerivation.publicKeyHex,
      chainLength: 1,
      merkleRoot: vectors.blockHashing.blockHash,
      packageVersion: '1.0',
      packagedAt: 1735689600, // 2025-01-01T00:00:00Z
      packagedBy: 'test-suite',
      hasNft: false,
    },
    genesisBlock: {
      block_number: 0,
      block_type: 'GENESIS',
      block_hash: vectors.blockHashing.blockHash,
      qube_id: 'F909521D',
      timestamp: 1735689600,
    },
    memoryBlocks: [],
    chainState: { version: '2.0', qube_id: 'F909521D' },
  };
}

describe('3a. Binary package format ↔ Python create_package()', () => {
  it('magic bytes are ASCII "QUBE" (0x51 0x55 0x42 0x45)', async () => {
    const { encrypted } = await createBinaryPackage(makePackageData());

    expect(encrypted[0]).toBe(0x51); // Q
    expect(encrypted[1]).toBe(0x55); // U
    expect(encrypted[2]).toBe(0x42); // B
    expect(encrypted[3]).toBe(0x45); // E
  });

  it('version byte at offset 4 is 0x01', async () => {
    const { encrypted } = await createBinaryPackage(makePackageData());
    expect(encrypted[4]).toBe(PACKAGE_VERSION);
    expect(encrypted[4]).toBe(0x01);
  });

  it('nonce occupies bytes 5–16 (12 bytes, matching NONCE_SIZE)', async () => {
    const { encrypted } = await createBinaryPackage(makePackageData());
    const nonce = encrypted.slice(5, 5 + NONCE_SIZE);
    expect(nonce.length).toBe(12);
  });

  it('total layout is [4 magic][1 version][12 nonce][plaintext_len + 16 tag]', async () => {
    const data = makePackageData();
    const plaintext = new TextEncoder().encode(JSON.stringify(data));
    const { encrypted } = await createBinaryPackage(data);

    const expectedLen = 4 + 1 + 12 + plaintext.length + 16;
    expect(encrypted.length).toBe(expectedLen);
  });

  it('createBinaryPackage → parseBinaryPackage round-trip restores data exactly', async () => {
    const original = makePackageData();
    const key = new Uint8Array(32).fill(0xab);
    const { encrypted } = await createBinaryPackage(original, key);
    const parsed = await parseBinaryPackage(encrypted, key);

    expect(parsed.metadata.qubeId).toBe(original.metadata.qubeId);
    expect(parsed.metadata.qubeName).toBe(original.metadata.qubeName);
    expect(parsed.metadata.merkleRoot).toBe(original.metadata.merkleRoot);
    expect(parsed.genesisBlock).toEqual(original.genesisBlock);
    expect(parsed.memoryBlocks).toEqual(original.memoryBlocks);
    expect(parsed.chainState).toEqual(original.chainState);
  });
});

describe('3b. .qube ZIP format ↔ Python export_qube_to_file()', () => {
  it('ZIP contains exactly manifest.json and data.enc', async () => {
    const zipBytes = await createQubeFile(makePackageData(), 'password');
    const files = unzipSync(zipBytes);

    expect(files['manifest.json']).toBeDefined();
    expect(files['data.enc']).toBeDefined();
    // No extra files
    expect(Object.keys(files)).toHaveLength(2);
  });

  it('manifest.json has qubeId, salt (32 hex chars), and nonce (24 hex chars)', async () => {
    const data = makePackageData();
    const zipBytes = await createQubeFile(data, 'test-password');
    const files = unzipSync(zipBytes);
    const manifest = JSON.parse(new TextDecoder().decode(files['manifest.json']));

    expect(manifest.qubeId).toBe(data.metadata.qubeId);
    // Salt = 16 bytes = 32 hex chars
    expect(manifest.salt).toMatch(/^[0-9a-f]{32}$/);
    // Nonce = 12 bytes = 24 hex chars
    expect(manifest.nonce).toMatch(/^[0-9a-f]{24}$/);
    // Version matches Python's "1.0"
    expect(manifest.version).toBe('1.0');
  });

  it('data.enc length is plaintext_len + 16 (no nonce prepended, nonce is in manifest)', async () => {
    const data = makePackageData();
    const plaintext = new TextEncoder().encode(JSON.stringify(data));
    const zipBytes = await createQubeFile(data, 'pw');
    const files = unzipSync(zipBytes);

    // Python stores the nonce in manifest.json and data.enc is ciphertext-only
    expect(files['data.enc'].length).toBe(plaintext.length + 16);
  });

  it('createQubeFile → parseQubeFile round-trip restores data exactly', async () => {
    const original = makePackageData();
    const password = 'cross-compat-test-password';
    const zipBytes = await createQubeFile(original, password);
    const parsed = await parseQubeFile(zipBytes, password);

    expect(parsed.metadata.qubeId).toBe(original.metadata.qubeId);
    expect(parsed.genesisBlock).toEqual(original.genesisBlock);
    expect(parsed.chainState).toEqual(original.chainState);
  });

  it('wrong password fails decryption (simulates Python receiving a TS-created file)', async () => {
    const zipBytes = await createQubeFile(makePackageData(), 'correct-password');
    await expect(parseQubeFile(zipBytes, 'wrong-password')).rejects.toThrow(/decrypt/i);
  });
});

